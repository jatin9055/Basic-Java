package ab53;

public class LogicalOperator {

	public static void main(String[] args) {
		
		
		int a = 100;
		int b = 20;
		 // and operator
		if(a>80 && b !=a)
		{
			System.out.println("AND operator");
		}
		
		// or operator
		if(a>b || b!=a)
		{
			System.out.println("OR operator");
		}
		
		
		//and with not operator 
		
		
	}

}
